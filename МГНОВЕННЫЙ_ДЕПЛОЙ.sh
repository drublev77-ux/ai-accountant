#!/bin/bash

# ═══════════════════════════════════════════════════════════════
# ⚡ МГНОВЕННЫЙ ДЕПЛОЙ БЕЗ GITHUB
# ═══════════════════════════════════════════════════════════════

set -e

# Цвета
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
PURPLE='\033[0;35m'
CYAN='\033[0;36m'
NC='\033[0m'

clear
echo -e "${PURPLE}"
cat << "EOF"
╔═══════════════════════════════════════════════════════════╗
║                                                           ║
║        ⚡ МГНОВЕННЫЙ ДЕПЛОЙ НА VERCEL ⚡                 ║
║                                                           ║
║           БЕЗ GitHub! БЕЗ токенов! 1 команда!            ║
║                                                           ║
╚═══════════════════════════════════════════════════════════╝
EOF
echo -e "${NC}\n"

echo -e "${CYAN}Проверка Vercel CLI...${NC}\n"

# Проверка Vercel CLI
if ! command -v vercel &> /dev/null; then
    echo -e "${YELLOW}Vercel CLI не установлен. Устанавливаю...${NC}\n"
    npm install -g vercel
    echo -e "${GREEN}✅ Vercel CLI установлен!${NC}\n"
fi

# Деплой
echo -e "${CYAN}🚀 Запуск деплоя на Vercel...${NC}\n"
echo -e "${YELLOW}При первом запуске:${NC}"
echo "  1. Нажмите Enter для входа в браузер"
echo "  2. Авторизуйтесь в Vercel"
echo "  3. Вернитесь в терминал"
echo ""

vercel --prod

echo -e "\n${GREEN}"
cat << "EOF"
╔═══════════════════════════════════════════════════════════╗
║                                                           ║
║              🎉 ДЕПЛОЙ ЗАВЕРШЕН! 🎉                     ║
║                                                           ║
╚═══════════════════════════════════════════════════════════╝
EOF
echo -e "${NC}\n"

echo -e "${GREEN}✅ Ваше приложение опубликовано!${NC}"
echo -e "${CYAN}🌐 Ссылка выше в выводе Vercel${NC}\n"
echo -e "${YELLOW}Для обновления запустите этот скрипт снова!${NC}\n"
